# a list of arithmetic operators
print(10 + 2)  # addition operator
print(10 - 2)  # subtraction operator
print(10 * 2)  # multiplication operator
print(float(10 / 3))  # division operator that returns a float
print(10 // 3)  # division operator that returns a integer
# modulus operator that returns the remainder fo whatever has been divided
print(10 % 3)
print(10 ** 3)  # exponent turns 10 to the power of three
x = 17
x -= 7  # this is an augmented operator
print(x)
